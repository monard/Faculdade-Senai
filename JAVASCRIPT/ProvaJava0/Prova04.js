let preco = Number(prompt('Digite a opção 0 - Barata , 1 - Moderado , 2 - Caro: '))
switch (preco){
    case 0: 
   console.log('Barato')
   break

   case 1: 
   console.log('Moderado')
   break

   case 2: 
   console.log('Caro')
   break

   default:
    console.log ('Numero Invalido')
}